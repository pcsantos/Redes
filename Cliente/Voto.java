public class Voto {

	private int codigo;
	private int nroVotos;

	public int getCodigo() {
		return this.codigo;
	}

	public int getNroVotos() {
		return this.nroVotos;
	}

	public void setCodigo(int codigo) {
		this.codigo = codigo;
	}
	
	public void setNroVotos(int nroVotos) {
		this.nroVotos = nroVotos;
	}
}
